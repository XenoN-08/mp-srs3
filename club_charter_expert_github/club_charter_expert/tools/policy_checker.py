DEFAULT_POLICY = """
ПРАВИЛА УНИВЕРСИТЕТА ДЛЯ СТУДЕНЧЕСКИХ КЛУБОВ И ОРГАНИЗАЦИЙ
(Версия 2024-2025, Отдел по работе со студентами)

1. РЕГИСТРАЦИЯ И НАИМЕНОВАНИЕ
   1.1. Каждый клуб обязан иметь официальное наименование, не совпадающее с уже зарегистрированными.
   1.2. Устав должен быть представлен в Студенческий совет не позднее 30 дней с момента основания.
   1.3. Устав утверждается решением Студенческого совета и проректора по воспитательной работе.

2. ЦЕЛИ И ДЕЯТЕЛЬНОСТЬ
   2.1. Цели клуба должны соответствовать образовательной миссии университета.
   2.2. Запрещается деятельность, противоречащая законодательству и этическим нормам.
   2.3. Цели и задачи должны быть конкретными, измеримыми и достижимыми.

3. ЧЛЕНСТВО
   3.1. Членство открыто для всех студентов без дискриминации по полу, расе, национальности,
        религии, сексуальной ориентации или инвалидности.
   3.2. Устав должен описывать процедуру вступления и выхода из клуба.
   3.3. Численность действительных членов — не менее 10 человек для регистрации.
   3.4. Должна быть предусмотрена процедура исключения с правом апелляции.

4. РУКОВОДСТВО
   4.1. В уставе обязательно определяются руководящие должности (минимум: председатель и казначей).
   4.2. Руководители избираются голосованием членов клуба не реже одного раза в год.
   4.3. Запрещается занимать должность более двух сроков подряд.
   4.4. Должны быть предусмотрены процедуры отзыва руководителя.

5. ФИНАНСИРОВАНИЕ
   5.1. Клуб может получать финансирование от Студенческого совета, спонсоров и за счёт взносов.
   5.2. Расходование средств свыше 5 000 руб. требует решения общего собрания.
   5.3. Ежегодный финансовый отчёт представляется в бухгалтерию университета.
   5.4. Запрещается использование средств клуба в личных целях.

6. ЭТИКА И ПОВЕДЕНИЕ
   6.1. Все члены клуба обязаны соблюдать Кодекс этики университета.
   6.2. Запрещается харассмент, буллинг, дискриминация в любых формах.
   6.3. Нарушения этических норм влекут санкции вплоть до исключения из клуба.
   6.4. В уставе должен быть описан механизм подачи жалоб.

7. БЕЗОПАСНОСТЬ
   7.1. При проведении мероприятий обязательно соблюдение правил пожарной безопасности.
   7.2. Мероприятия с числом участников более 50 человек требуют согласования с охраной.
   7.3. Запрещается проведение мероприятий в состоянии алкогольного опьянения.
   7.4. Клуб несёт ответственность за безопасность на своих мероприятиях.

8. ОТЧЁТНОСТЬ И ДОКУМЕНТАЦИЯ
   8.1. Протоколы всех заседаний хранятся не менее 3 лет.
   8.2. Ежегодный отчёт о деятельности представляется в Студенческий совет.
   8.3. Устав пересматривается не реже одного раза в три года.

9. ПРЕКРАЩЕНИЕ ДЕЯТЕЛЬНОСТИ
   9.1. Клуб может быть ликвидирован по решению 2/3 членов или по решению администрации.
   9.2. При ликвидации активы передаются Студенческому совету или на благотворительность.
   9.3. Все долги клуба должны быть погашены до окончания ликвидации.

10. ИЗМЕНЕНИЕ УСТАВА
    10.1. Изменения в устав вносятся на общем собрании с кворумом не менее 50%+1.
    10.2. Изменения вступают в силу после утверждения Студенческим советом.
    10.3. Не допускается изменение устава в период выборов.
"""


